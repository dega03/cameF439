/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
	//Init pointers to the codes read on the Came board TIM7
	NewCodePtr = 0;
	NewCode2ReadPtr = 0;
	uint32_t LastCPUStatusCheck,LastCameOp;
	uint32_t CurrTimestamp;

	memset(&CodeData[0],0xff,10);  //Clear current buffer

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART3_UART_Init();
  MX_LWIP_Init();
  MX_CRC_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM5_Init();
  /* USER CODE BEGIN 2 */


  //TImers 1 and 2 used in cascade to generate timestamp for ethernet messages
  LL_TIM_EnableCounter(TIM2);
  LL_TIM_EnableCounter(TIM1);

  LL_TIM_EnableCounter(TIM5); //Counts at 1MHz, to generate schedule in main.c
  /* USER CODE BEGIN 5 */
  SWIMInit();

  // Inith ethernet interface
  eth_interface_init();

#ifdef Debug  //Print memory
  uint32_t RxAddr = 0x5;
  uint8_t RxChar;
#endif

  CurrTimestamp = LL_TIM_GetCounter(TIM5);
  LastCPUStatusCheck = CurrTimestamp;
  LastCameOp = CurrTimestamp;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

	  CurrTimestamp = LL_TIM_GetCounter(TIM5);
#ifdef Debug  //Print memory

	  i = 0;
//	  SWIM_Write(0x7f99, 1,(uint16_t *)&Data);
	  SWIM_Read((RxAddr<<4),0x40,(uint16_t *)&Data);
//	  SWIMRst();
	  sprintf(TxBuffer,"%05X > ", (unsigned int)RxAddr<<4);
	  LL_PrintMyString(TxBuffer,0);
	  while(i<32) {
		  sprintf(TxBuffer,"%02X %02X %02X %02X.%02X %02X %02X %02X|", Data[i] & 0xffff, (Data[i]>>16) & 0xffff, Data[i+1] & 0xffff, (Data[i+1]>>16) & 0xffff, Data[i+2] & 0xffff, (Data[i+2]>>16) & 0xffff,Data[i+3] & 0xffff, (Data[i+3]>>16) & 0xffff);
		  i += 4;
		  if (i<32)
			  LL_PrintMyString(TxBuffer,0);
		  else
			  LL_PrintMyString(TxBuffer,1);
	  }
#endif
	  if (SWIMStatus != SWIM_Idle) {
		  SWIMCommRst();
		  if (SWIMStatus != SWIM_Idle) { //not recovered? try a full resync
			  LL_GPIO_SetOutputPin(LD3_GPIO_Port, LD3_Pin);
			  SWIMInit();
			  LastCPUStatusCheck = CurrTimestamp - 3100000l; //To force check of CPU stall
		  }
	  } else {
		  LL_GPIO_ResetOutputPin(LD3_GPIO_Port, LD3_Pin);
	  }
#ifdef Debug  //Print memory
	  if (LL_USART_IsActiveFlag_RXNE(USART3)) {
		  RxChar = LL_USART_ReceiveData8(USART3) | 0x20; //Converted to lowercase
		  if (RxChar >= 'a')
			  RxChar = RxChar - 'a' + 10;
		  else
			  RxChar = RxChar - '0';
		  RxAddr = ((RxAddr<<4) | RxChar) & 0xffff;
	  }
	  osDelay(500);
#endif

	  if ((CurrTimestamp - LastCPUStatusCheck) > 3000000l) {  //every 3 sec check if came is in reset
		  LastCPUStatusCheck = CurrTimestamp;
		  SWIM_Read(0x7f99, 1,(uint16_t *)&Data);  //Checking if CPU is in stall, normally after reset
		  if ((Data[0] & 8 ) == 8) {
			  Data[0] = Data[0] & 0xF7;  //clear bit 3 to resume CPU from stall
			  SWIM_Write(0x7f99, 1,(uint16_t *)&Data);

		  }
	  }
	  //Print timestamp
	  //sprintf(TxBuffer,"%08lX",LL_TIM_GetCounter(TIM2));
	  //LL_PrintMyString(TxBuffer,1);

#ifndef Debug
	  if ((CurrTimestamp - LastCameOp) > 100000l) {
		  CheckStoreCode();

		  if (CameStatus == CameOpRead) {
			  LL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
			  ReadFlashCode();
		  }

		  if (CameStatus == CameOpWrite) {
			  LL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
			  WriteFlashCode();
		  }
		  LastCameOp = CurrTimestamp;
	  }
#endif

	  //LL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
	  //osDelay(100);


//	  SWIMRst();
//	  osDelay(1);
//	  LL_GPIO_ResetOutputPin(LD3_GPIO_Port, LD3_Pin);
//	  LL_GPIO_ResetOutputPin(LD1_GPIO_Port, LD1_Pin);


	  MX_LWIP_Process();
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

